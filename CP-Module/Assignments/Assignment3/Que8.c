#include <stdio.h>

int main() {
    int num = 145, temp, digit, sum = 0;
    int fact, i;

    temp = num;

    while (temp > 0) {
        digit = temp % 10;

        fact = 1;
        for (i = 1; i <= digit; i++) {
            fact = fact * i;
        }

        sum = sum + fact;
        temp = temp / 10;
    }

    if (sum == num)
        printf("Strong");
    else
        printf("Not Strong");

    return 0;
}